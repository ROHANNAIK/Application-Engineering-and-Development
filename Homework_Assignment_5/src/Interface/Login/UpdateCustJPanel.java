    private void savejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savejButtonActionPerformed
        // TODO add your handling code here:
          String hashedPassword = UserClass.generateHash(updatecustjPasswordField.getText());
        try {
            UserClass uc = (UserClass) updateChoosejComboBox.getSelectedItem();
            //Person p = b.getPersonDirect().searchPerson(name);
            //UserClass uc = b.getUserAccDirect().addUserClass();
            uc.setPassword(hashedPassword);
            uc.setUserAccType(updateRolejTextField.getText());
            if (buttonGroup2.isSelected(activejRadioButton.getModel())) {
                uc.setFlag(true);
            } else {
                uc.setFlag(false);
            }
            savejButton.setEnabled(false);
            updatejButton.setEnabled(true);
            UIManager.put("OptionPane.messageFont", new Font("Arial", Font.BOLD, 24));
            JOptionPane.showMessageDialog(null, "Account updated successfully");
        } catch (Exception e) {
            UIManager.put("OptionPane.messageFont", new Font("Arial", Font.BOLD, 24));
            JOptionPane.showMessageDialog(null, "Invalid Entry.Please enter correct values", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_savejButtonActionPerformed

