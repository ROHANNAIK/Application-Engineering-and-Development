    private void statechanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_statechanged
        // TODO add your handling code here:
        int state = evt.getStateChange();
        if(state == ItemEvent.SELECTED){
            UserClass uc = (UserClass) updateChoosejComboBox.getSelectedItem();
            unamejTextField.setText(uc.getUserName());
            updatecustjPasswordField.setText(uc.getPassword());
            updateRolejTextField.setText(uc.getUserAccType());
        }
    }//GEN-LAST:event_statechanged

